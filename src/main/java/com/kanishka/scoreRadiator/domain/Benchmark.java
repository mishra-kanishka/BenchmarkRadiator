package com.kanishka.scoreRadiator.domain;

public class Benchmark {

    private String PCMark10Score;

    private String EssentialsScore;

    private String DigitalContentCreationScore;

    private String ProductivityScore;

    public String getPCMark10Score() {
        return PCMark10Score;
    }

    public void setPCMark10Score(String PCMark10Score) {
        this.PCMark10Score = PCMark10Score;
    }

    public String getEssentialsScore() {
        return EssentialsScore;
    }

    public void setEssentialsScore(String essentialsScore) {
        EssentialsScore = essentialsScore;
    }

    public String getDigitalContentCreationScore() {
        return DigitalContentCreationScore;
    }

    public void setDigitalContentCreationScore(String digitalContentCreationScore) {
        DigitalContentCreationScore = digitalContentCreationScore;
    }

    public String getProductivityScore() {
        return ProductivityScore;
    }

    public void setProductivityScore(String productivityScore) {
        ProductivityScore = productivityScore;
    }
}
